package com.ai.takeaway.model;

public class Restaurant {

	private int res_id;
	private String res_name;
	private String res_menu_link;
	private String res_info;
	
	public Restaurant(){
		
	}

	public int getRes_id() {
		return res_id;
	}

	public void setRes_id(int res_id) {
		this.res_id = res_id;
	}

	public String getRes_name() {
		return res_name;
	}

	public void setRes_name(String res_name) {
		this.res_name = res_name;
	}

	public String getRes_menu_link() {
		return res_menu_link;
	}

	public void setRes_menu_link(String res_menu_link) {
		this.res_menu_link = res_menu_link;
	}

	public String getRes_info() {
		return res_info;
	}

	public void setRes_info(String res_info) {
		this.res_info = res_info;
	}

	
}

